pub fn parse_extended_packet() -> ruptela_listener::protocol::Packet {
    let data = hex(EXTENDED_RECORDS_PACKET_HEX);
    let packet_len = u16::from_be_bytes([data[0], data[1]]) as usize;
    let body = &data[2..2 + packet_len];
    ruptela_listener::protocol::parse_packet(body).expect("parse_packet 0x44 failed")
}
